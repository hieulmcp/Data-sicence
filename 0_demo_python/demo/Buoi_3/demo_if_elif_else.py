

dtb = 6

if 0 <= dtb < 11:
    if dtb < 5:
        print("Yếu / Kém")
    elif dtb < 6:
        print("Trung bình")
    elif dtb < 7:
        print("Trung bình - Khá")
    elif dtb < 8:
        print("Khá")
    elif dtb < 9:
        print("Giỏi")
    elif dtb <= 10:
        print("Xuất sắc")
else:
    print("Điểm không hợp lệ!")




